CREATE OR REPLACE VIEW VW_RENTAS_POR_ACTIVO AS
    SELECT 
        T3.TICKER,
        T3.NOMBRE_ACTIVO,
        SUM(T1.MONTO_NETO) AS TOTAL_COBRADO_NETO,
        COUNT(T1.ID_RENTA) AS CANTIDAD_DE_COBROS
    FROM DIVIDENDOS_CUPONES T1
        JOIN ACTIVOS T3 ON T1.ID_ACTIVO = T3.ID_ACTIVO
    GROUP BY T3.TICKER, T3.NOMBRE_ACTIVO;
    
# SELECT * FROM VW_RENTAS_POR_ACTIVO WHERE NOMBRE_ACTIVO = "SPY";

CREATE OR REPLACE VIEW VW_CAUCIONES_VIGENTES AS
    SELECT 
		T2.ID_USUARIO,
        T2.NOMBRE_COMPLETO_USUARIO,
        T1.MONTO_COLOCADO,
        T1.TASA_NOMINAL_ANUAL,
        T1.FECHA_FIN,
        DATEDIFF(T1.FECHA_FIN, CURDATE()) AS DIAS_PARA_EL_VENCIMIENTO
    FROM CAUCIONES_OPERADAS T1
        JOIN USUARIOS T2 ON T1.ID_USUARIO = T2.ID_USUARIO
    WHERE T1.FECHA_FIN >= CURDATE();
    
