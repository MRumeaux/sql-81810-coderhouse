CREATE FUNCTION FN_CALCULAR_INTERES_CAUCION_AUTO(MANUAL_ID_CAUCION INT) 
RETURNS DECIMAL(15,2)
READS SQL DATA
BEGIN
    DECLARE V_MONTO DECIMAL(15,2);
    DECLARE V_TNA DECIMAL(5,2);
    DECLARE V_DIAS INT;
    DECLARE V_INTERESES DECIMAL(15,2);
    SELECT MONTO_COLOCADO, TASA_NOMINAL_ANUAL, DATEDIFF(FECHA_FIN, FECHA_INICIO)
		INTO V_MONTO, V_TNA, V_DIAS
	FROM CAUCIONES_OPERADAS
    WHERE ID_CAUCION = MANUAL_ID_CAUCION;
    SET V_INTERESES = (V_MONTO * (V_TNA / 100) * (V_DIAS / 365));
    RETURN V_INTERESES;
END //

CREATE FUNCTION FN_GET_NOMBRE_BROKER(MANUAL_ID_BROKER INT) 
RETURNS VARCHAR(50)
READS SQL DATA
BEGIN
    DECLARE V_NOMBRE VARCHAR(50);
    SELECT NOMBRE_BROKER 
		INTO V_NOMBRE
    FROM BROKERS
    WHERE ID_BROKER = MANUAL_ID_BROKER;
    RETURN V_NOMBRE;
END //

