CREATE OR REPLACE VIEW VW_PORTAFOLIO_DETALLADO AS
    SELECT
		T2.ID_USUARIO,
        T2.NOMBRE_COMPLETO_USUARIO,
        T5.NOMBRE_SECTOR,
        T3.TIPO_ACTIVO,
        T3.TICKER,
        T3.NOMBRE_ACTIVO,
        T1.CANTIDAD_TOTAL,
        T1.ULTIMO_PRECIO,
        (T1.CANTIDAD_TOTAL * T1.ULTIMO_PRECIO) AS VALOR_TOTAL_ACTIVO
    FROM PORTFOLIO T1 
        JOIN USUARIOS T2 ON T1.ID_USUARIO = T2.ID_USUARIO
        JOIN ACTIVOS T3 ON T1.ID_ACTIVO = T3.ID_ACTIVO
        JOIN SECTORES T5 ON T3.ID_SECTOR = T5.ID_SECTOR
    ORDER BY 
        T2.NOMBRE_COMPLETO_USUARIO ASC, 
        T5.NOMBRE_SECTOR DESC, 
        T3.TICKER DESC;

