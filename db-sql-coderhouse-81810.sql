CREATE OR REPLACE VIEW VW_CASH_DISPONIBLE AS
    SELECT 
		T2.ID_USUARIO,
        T2.NOMBRE_COMPLETO_USUARIO,
        T3.NOMBRE_BROKER,
        T4.CODIGO AS MONEDA,
        T1.SALDO_DISPONIBLE
    FROM SALDO_DISPONIBLE_BROKERS T1
        JOIN USUARIOS T2 ON T1.ID_USUARIO = T2.ID_USUARIO
        JOIN BROKERS T3 ON T1.ID_BROKER = T3.ID_BROKER
        JOIN MONEDA T4 ON T1.ID_MONEDA = T4.ID_MONEDA;
        
