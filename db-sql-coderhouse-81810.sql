CREATE PROCEDURE SP_ORDENAR_TABLA_DINAMICO(IN NOMBRE_TABLA VARCHAR(50), IN NOMBRE_COLUMNA VARCHAR(50), IN SENTIDO_ORDENAMIENTO VARCHAR(4))
BEGIN
    SET @SENTENCIA = CONCAT('SELECT * FROM ', NOMBRE_TABLA, ' ORDER BY ', NOMBRE_COLUMNA, ' ', SENTIDO_ORDENAMIENTO);
    PREPARE EJECUTAR FROM @SENTENCIA;
    EXECUTE EJECUTAR;
    DEALLOCATE PREPARE EJECUTAR;
END //

CREATE PROCEDURE SP_INSERTAR_ACTIVO(
    IN V_TICKER VARCHAR(10), 
    IN V_NOMBRE VARCHAR(30), 
    IN V_TIPO VARCHAR(30), 
    IN MANUAL_ID_SECTOR INT
)
BEGIN
    INSERT INTO ACTIVOS (TICKER, NOMBRE_ACTIVO, TIPO_ACTIVO, ID_SECTOR)
    VALUES (V_TICKER, V_NOMBRE, V_TIPO, MANUAL_ID_SECTOR);
    SELECT CONCAT('Activo ', V_TICKER, ' insertado con éxito') AS resultado;
END //

-- TRIGGERS --

CREATE TRIGGER TR_ACTUALIZAR_PORTFOLIO_AFTER_INSERT
AFTER INSERT ON ORDENES_COMPRA_VENTA
FOR EACH ROW
BEGIN
    INSERT INTO PORTFOLIO (ID_USUARIO, ID_ACTIVO, CANTIDAD_TOTAL, ULTIMO_PRECIO)
    VALUES (
        NEW.ID_USUARIO, 
        NEW.ID_ACTIVO, 
        CASE WHEN NEW.TIPO_OPERACION = 'COMPRA' THEN NEW.CANTIDAD ELSE -NEW.CANTIDAD END, 
        NEW.PRECIO_UNITARIO
    )
    ON DUPLICATE KEY UPDATE 
        CANTIDAD_TOTAL = CASE 
            WHEN NEW.TIPO_OPERACION = 'COMPRA' THEN CANTIDAD_TOTAL + NEW.CANTIDAD 
            ELSE CANTIDAD_TOTAL - NEW.CANTIDAD 
        END,
        ULTIMO_PRECIO = NEW.PRECIO_UNITARIO;
END //

CREATE TRIGGER TR_LOG_AUDITORIA_ORDENES_AFTER_INSERT
AFTER INSERT ON ORDENES_COMPRA_VENTA
FOR EACH ROW
BEGIN
    INSERT INTO LOG_OPERACIONES (ACCION, TABLA_AFECTADA, USUARIO_ID, FECHA_HORA)
    VALUES ('INSERT', 'ORDENES_COMPRA_VENTA', NEW.ID_USUARIO, CURRENT_TIMESTAMP);
END //

