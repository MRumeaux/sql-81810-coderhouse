CREATE OR REPLACE VIEW VW_ANALISIS_COMISIONES AS
    SELECT 
		T2.ID_USUARIO,
        T2.NOMBRE_COMPLETO_USUARIO,
        T3.NOMBRE_BROKER,
        SUM(T1.COMISION_PAGADA) AS TOTAL_COMISIONES_PAGADAS,
        AVG(T1.COMISION_PAGADA) AS COMISION_PROMEDIO_POR_ORDEN
    FROM ORDENES_COMPRA_VENTA T1
        JOIN USUARIOS T2 ON T1.ID_USUARIO = T2.ID_USUARIO
        JOIN BROKERS T3 ON T1.ID_BROKER = T3.ID_BROKER
    GROUP BY T2.NOMBRE_COMPLETO_USUARIO, T3.NOMBRE_BROKER;
    
